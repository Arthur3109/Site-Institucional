function showLoader() {
    const loader = document.getElementById('page-loader');
    if (loader) {
        // Garante que o loader est√° vis√≠vel e acima de tudo
        loader.classList.remove('hidden');
        loader.style.display = 'flex'; 
    }
}

function hideLoader() {
    const loader = document.getElementById('page-loader');
    if (loader) {
        // Inicia a transi√ß√£o de opacidade para ocultar
        loader.classList.add('hidden'); 
        
        // Remove ou oculta totalmente ap√≥s a transi√ß√£o CSS (600ms)
        setTimeout(() => {
            loader.style.display = 'none';
        }, 600); 
    }
}


