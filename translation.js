function setLanguage(lang) {
    // 1. MOSTRA O LOADER antes de iniciar a tradu√ß√£o
    showLoader(); 

    // Simula um pequeno atraso para que o usu√°rio veja o loader antes da tradu√ß√£o instant√¢nea
    setTimeout(() => {
        const elements = document.querySelectorAll('[data-i18n]');
        
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            
            // Certifique-se de que a vari√°vel 'translations' esteja definida globalmente
            if (typeof translations !== 'undefined' && translations[lang] && translations[lang][key]) {
                const translatedText = translations[lang][key];

                element.innerHTML = translatedText;
                
                if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                    // Remove tags HTML do placeholder, se houver
                    element.setAttribute('placeholder', translatedText.replace(/(<([^>]+)>)/gi, ""));
                }
            }
        });
        
        localStorage.setItem('preferredLanguage', lang);
        
        // 2. ESCONDE O LOADER ap√≥s a tradu√ß√£o
        hideLoader();

    }, 200); // 200ms de "tempo de processamento" de tradu√ß√£o.
}


